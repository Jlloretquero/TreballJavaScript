function cambiarIframe($parametro){

    if ($parametro == 1){
        document.getElementById("iframe").src="Jardin/Jardin.html"
    }
  
    else if ($parametro == 2){
        document.getElementById("iframe").src="Limpieza/Limpieza.html"
    }

     else if ($parametro == 3){
        document.getElementById("iframe").src="Menaje/Menaje.html"
    }


}